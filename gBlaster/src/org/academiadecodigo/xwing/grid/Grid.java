package org.academiadecodigo.xwing.grid;

public class Grid {

    private int cols;
    private int rows;




    //CONSTRUCTOR

    public Grid (int cols, int rows) {
        this.cols = cols;
        this.rows = rows;

    }


    // SET & GET

    public int getCols () {
        return cols;
    }

    public int getRows () {
        return rows;
    }


    // GFX METHODS



}
